    private void btn_CapNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CapNhapActionPerformed
        try{
            if (!(tf_MatKhauCu.getText().trim().length() == 0))
                if (!(tf_MatKhauMoi.getText().trim().length() == 0))
                    if ((tf_NhapLai.getText().trim().equals(tf_MatKhauMoi.getText().trim())))
                        if (BTaiKhoan.getInstance().CheckDangNhap(tf_TenDN.getText(), tf_MatKhauCu.getText()))
                            if (BTaiKhoan.getInstance().UpdateTaiKhoan(tf_TenDN.getText(), tf_MatKhauMoi.getText())) {
                                JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công");
                                tf_MatKhauCu.setText(null);
                                tf_MatKhauMoi.setText(null);
                                tf_NhapLai.setText(null);
                                this.dispose();
                            }
                            else
                                JOptionPane.showMessageDialog(this, "Không thành công");
                        else
                            JOptionPane.showMessageDialog(this, "Mật khẩu cũ không đúng");
                    else
                        JOptionPane.showMessageDialog(this, "Mật khẩu mới không khớp");
                else
                    JOptionPane.showMessageDialog(this, "Mật khẩu mới không được để trống");
            else
                JOptionPane.showMessageDialog(this, "Mật khẩu cũ không được để trống");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "ERROR: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_CapNhapActionPerformed

