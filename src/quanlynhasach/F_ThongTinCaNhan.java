    private void lb_ShowPassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lb_ShowPassMouseClicked
        if (tf_MatKhauCu.getEchoChar() != '\u25cf'){
            tf_MatKhauCu.setEchoChar('\u25cf');
            tf_MatKhauMoi.setEchoChar('\u25cf');
            tf_NhapLai.setEchoChar('\u25cf');
            lb_ShowPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/eye.png")));
        }
        else{
            tf_MatKhauCu.setEchoChar((char)0);
            tf_MatKhauMoi.setEchoChar((char)0);
            tf_NhapLai.setEchoChar((char)0);
            lb_ShowPass.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/icons8_invisible_20px.png")));
        }
    }//GEN-LAST:event_lb_ShowPassMouseClicked

